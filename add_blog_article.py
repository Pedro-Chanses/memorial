# Функція для додавання статті в базу даних
def add_blog_article():
    with app.app_context():
        # Знаходимо адміністратора для автора статті
        admin = User.query.filter_by(is_admin=True).first()
        
        if not admin:
            print("Помилка: Не знайдено адміністратора для автора статті")
            return
        
        # Створюємо нову статтю
        new_article = News(
            title=title,
            content=content,
            author_id=admin.id,
            category='blog',
            is_published=True,
            created_at=datetime.utcnow(),
            views=0
        )
        
        # Додаємо статтю в базу даних
        db.session.add(new_article)
        db.session.commit()
        
        print(f"Статтю '{title}' успішно додано в блог!")

if __name__ == "__main__":
    add_blog_article()
